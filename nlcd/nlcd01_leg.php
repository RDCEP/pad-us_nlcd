<p class="maintitle">National Land Cover Database 2001 (NLCD2001) </p>

<p class="maintitle2">Product Legend</p>
<p class="setpage">
The classification system used by NLCD2001 is modified from the <A class="legendlinks" href="http://landcover.usgs.gov/pdf/anderson.pdf">Anderson Land Cover Classification System</A>*.
<br><br>
<a class="legendlinks" target="_blank" href="downloadfile.php?file=NLCD_Colour_Classification_Update.jpg">Download</a> the NLCD2001 land cover classification legend.<br />
<A class="legendlinks" target="_blank" href="downloadfile.php?file=NLCD_imperv_legend.jpg">Download</A> the percent developed imperviousness legend.<br />
<a class="legendlinks" target="_blank" href="downloadfile.php?file=NLCD_canopy_legend.jpg">Download</a> the percent tree canopy legend. 
</p>
<br>

<div style="padding-left:10px;margin-left:10px;">
<table style="border:solid .5px #000000;width:75%;background-color:#000000;">
<tr>
<th style="background-color:#ffffff">Class\ Value</th><th style="background-color:#ffffff">Classification Description</th>
</tr>
<tr>
<td valign="top" class="white"><strong>Water</strong></td><td class="white" valign="top"><i>areas of open water or permanent ice/snow cover.</i></td></tr><tr>

<td valign="top" class="water" align="right">11</td><td class="water" valign="top"><strong>Open Water</strong> - areas of open water, generally with less than 25% cover of vegetation or soil.</td></tr><tr>

<td valign="top" class="white" align="right">12</td><td class="white" valign="top"><strong>Perennial Ice/Snow</strong> - areas characterized by a perennial cover of ice and/or snow, generally greater than 25% of total cover.</td></tr><tr>

<td valign="top" class="white"><strong>Developed</strong></td><td class="white" valign="top"><i>areas characterized by a high percentage (30% or greater) of constructed materials (e.g. asphalt, concrete, buildings, etc.).</i></td></tr><tr>

<td valign="top" class="lowintensity" align="right">21</td><td class="lowintensity" valign="top"><strong>Developed, Open Space</strong> - areas with a mixture of some constructed materials, but mostly vegetation in the form of lawn grasses. Impervious surfaces account for less than 20% of total cover. These areas most commonly include large-lot single-family housing units, parks, golf courses, and vegetation planted in developed settings for recreation, erosion control, or aesthetic purposes.
</td></tr><tr>

<td valign="top" class="highintensity" align="right">22</td><td class="highintensity" valign="top"><strong>Developed, Low Intensity</strong> - areas with a mixture of constructed materials and vegetation. Impervious surfaces account for 20% to 49% percent of total cover. These areas most commonly include single-family housing units.
</td></tr><tr>

<td valign="top" class="developed" align="right">23</td><td class="developed" valign="top"><strong>Developed, Medium Intensity </strong> – areas with a mixture of constructed materials and vegetation. Impervious surfaces account for 50% to 79% of the total cover. These areas most commonly include single-family housing units.
</td></tr><tr>

<td valign="top" class="devhighintensity" align="right">24</td><td class="devhighintensity" valign="top"><strong>Developed High Intensity</strong> -highly developed areas where people reside or work in high numbers. Examples include apartment complexes, row houses and commercial/industrial. Impervious surfaces account for 80% to 100% of the total cover.
</td></tr><tr>

<td valign="top" class="white"><strong>Barren</strong></td><td class="white" valign="top"><i>areas characterized by bare rock, gravel, sand, silt, clay, or other earthen material, with little or no "green" vegetation present regardless of its inherent ability to support life. Vegetation, if present, is more widely spaced and scrubby than that in the green vegetated categories; lichen cover may be extensive.</i></td></tr><tr>

<td valign="top" class="barerock" align="right">31</td><td class="barerock" valign="top"><strong>Barren Land (Rock/Sand/Clay) </strong> - areas of bedrock, desert pavement, scarps, talus, slides, volcanic material, glacial debris, sand dunes, strip mines, gravel pits and other accumulations of earthen material. Generally, vegetation accounts for less than 15% of total cover.
</td></tr><tr>

<td valign="top" class="white"><strong>Forest</strong></td><td class="white" valign="top"><i>areas characterized by tree cover (natural or semi-natural woody vegetation, generally greater than 6 meters tall); tree canopy accounts for 25% to 100%  of the cover.</i>
</td></tr><tr>

<td valign="top" class="deciduous" align="right">41</td><td class="deciduous" valign="top"><strong>Deciduous Forest</strong> - areas dominated by trees generally greater than 5 meters tall, and greater than 20% of total vegetation cover. More than 75% of the tree species shed foliage simultaneously in response to seasonal change.
</td></tr><tr>

<td valign="top" class="evergreen" align="right">42</td><td class="evergreen" valign="top"><strong>Evergreen Forest</strong> - areas dominated by trees generally greater than 5 meters tall, and greater than 20% of total vegetation cover. More than 75% of the tree species maintain their leaves all year. Canopy is never without green foliage.
</td></tr><tr>

<td valign="top" class="mixed" align="right">43</td><td class="mixed" valign="top"><strong>Mixed Forest</strong> - areas dominated by trees generally greater than 5 meters tall, and greater than 20% of total vegetation cover. Neither deciduous nor evergreen species are greater than 75% of total tree cover.
</td></tr><tr>

<td valign="top" class="white"><strong>Shrubland</strong></td><td class="white" valign="top"><i>areas characterized by natural or semi-natural woody vegetation with aerial stems, generally less than 6 meters tall, with individuals or clumps not touching to interlocking. Both evergreen and deciduous species of true shrubs, young trees, and trees or shrubs that are small or stunted because of environmental conditions are included.</i>.
</td></tr><tr>

<td valign="top" class="dwarf" align="right">51</td><td class="dwarf" valign="top"><strong>Dwarf Scrub</strong> - Alaska only areas dominated by shrubs less than 20 centimeters tall with shrub canopy typically greater than 20% of total vegetation. This type is often co-associated with grasses, sedges, herbs, and non-vascular vegetation.
</td></tr><tr>

<td valign="top" class="shrubland" align="right">52</td><td class="shrubland" valign="top"><strong>Shrub/Scrub</strong> - areas dominated by shrubs; less than 5 meters tall with shrub canopy typically greater than 20% of total vegetation. This class includes true shrubs, young trees in an early successional stage or trees stunted from environmental conditions.
</td></tr><tr>

<td valign="top" class="white" ><strong>Herbaceous</strong> </td><td class="white" valign="top"><i>areas characterized by natural or semi-natural herbaceous vegetation; herbaceous vegetation accounts for 75% to 100%  of the cover.</i>
</td></tr><tr>

<td valign="top" class="grasslands" align="right">71</td><td class="grasslands" valign="top"><strong>Grassland/Herbaceous</strong> - areas dominated by gramanoid or herbaceous vegetation, generally greater than 80% of total vegetation. These areas are not subject to intensive management such as tilling, but can be utilized for grazing.
</td></tr><tr>

<td valign="top" class="sedge" align="right">72</td><td class="sedge" valign="top"><strong>Sedge/Herbaceous</strong> - Alaska only areas dominated by sedges and forbs, generally greater than 80% of total vegetation. This type can occur with significant other grasses or other grass like plants, and includes sedge tundra, and sedge tussock tundra.
</td></tr><tr>

<td valign="top" class="lichens" align="right">73</td><td class="lichens" valign="top"><strong>Lichens</strong> - Alaska only areas dominated by fruticose or foliose lichens generally greater than 80% of total vegetation.</td></tr><tr>

<td valign="top" class="moss" align="right">74</td><td class="moss" valign="top"><strong>Moss</strong> - Alaska only areas dominated by mosses, generally greater than 80% of total vegetation.
</td></tr><tr>

<td valign="top" class="white"><strong>Planted/Cultivated</strong></td><td class="white" valign="top"><i>areas characterized by herbaceous vegetation that has been planted or is intensively managed for the production of food, feed, or fiber; or is maintained in developed settings for specific purposes. Herbaceous vegetation accounts for 75% to 100%  of the cover.</i></td></tr><tr>

<td valign="top" class="pasture" align="right">81</td><td class="pasture" valign="top"><strong>Pasture/Hay</strong> – areas of grasses, legumes, or grass-legume mixtures planted for livestock grazing or the production of seed or hay crops, typically on a perennial cycle. Pasture/hay vegetation accounts for greater than 20% of total vegetation.
</td></tr><tr>

<td valign="top" class="rowcrops" align="right">82</td><td class="rowcrops" valign="top"><strong>Cultivated Crops </strong> – areas used for the production of annual crops, such as corn, soybeans, vegetables, tobacco, and cotton, and also perennial woody crops such as orchards and vineyards. Crop vegetation accounts for greater than 20% of total vegetation. This class also includes all land being actively tilled.</td></tr><tr>

<td valign="top" class="white"><strong>Wetlands</strong></td><td class="white" valign="top"><i>areas where the soil or substrate is periodically saturated with or covered with water as defined by Cowardin et al., (1979).</i></td></tr><tr>

<td valign="top" class="woodywetlands" align="right">90</td><td class="woodywetlands" valign="top"><strong>Woody Wetlands</strong> - areas where forest or shrubland vegetation accounts for greater than 20% of vegetative cover and the soil or substrate is periodically saturated with or covered with water.</td></tr><tr>

<td valign="top" class="emergentwetlands" align="right">95</td><td class="emergentwetlands" valign="top"><strong>Emergent Herbaceous Wetlands</strong> - Areas where perennial herbaceous vegetation accounts for greater than 80% of vegetative cover and the soil or substrate is periodically saturated with or covered with water.</td>



</tr>
